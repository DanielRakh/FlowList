import UIKit

//MARK:
//MARK: Flat Colors
extension UIColor {
    
    class func FLCMightnightBlue() -> UIColor {
        return UIColor(red:0.180, green:0.184 , blue:0.231, alpha: 1.0) }
    
    class func FLCHazyBlue() -> UIColor {
        return UIColor(red:0.231, green:0.231 , blue:0.282, alpha: 1.0) }
    
    class func FLCElectricBlue() -> UIColor {
        return UIColor(red:0.224, green:0.506 , blue:0.906, alpha: 1.0) }
    
    class func FLCGreen() -> UIColor {
        return UIColor(red:0.286, green:0.831 , blue:0.486, alpha: 1.0) }
    
    class func FLCUnselectedGray() -> UIColor {
        return UIColor(red:0.463, green:0.463 , blue:0.498, alpha: 1.0) }
    
    class func FLCPlainWhite() -> UIColor {
        return UIColor(red:1.000, green:1.000 , blue:1.000, alpha: 1.0) }
    
    class func FLCPlainBlack() -> UIColor {
        return UIColor(red:0.000, green:0.000 , blue:0.000, alpha: 1.0) }
}